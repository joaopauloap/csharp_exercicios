/*
1. O que é um objeto?    
    R: Objeto é um tipo de váriavel composta por pares de um atributo e o seu valor (que pode ser uma função, 
       chamada nesse contexto de Método).

2. O que é uma classe?    
    R: Classe é um molde para criar um objeto. Nela já estão predefinidos os atributos e os Métodos que 
       irão manipular estes atributos deste ou de outros objetos.

3. Qual a relação entre classes e objetos?
    R: Objetos são criados apartir das classes, herdando seus tipos pré-definidos.

4. O que é um atributo/propriedade?
    R: Atributo é o nome dado as variáveis do objeto.

5. O que é um método?
    R: Métodos são as funções que estão como o valor de um atributo, dentro de um objeto.
*/